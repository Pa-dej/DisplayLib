/**
 * Lua API для программирования логики экранов.
 * 
 * <p>Этот пакет содержит все классы API, доступные в Lua скриптах.
 * Каждый класс предоставляет набор методов для взаимодействия с различными
 * аспектами игры и экранов.</p>
 * 
 * <h2>Доступные API в Lua:</h2>
 * 
 * <h3>player - {@link padej.displayLib.lua.api.PlayerAPI}</h3>
 * <p>Работа с игроком:</p>
 * <ul>
 * <li>Получение информации (имя, режим игры, здоровье)</li>
 * <li>Отправка сообщений и воспроизведение звуков</li>
 * <li>Выполнение команд от имени игрока</li>
 * <li>Изменение состояния (режим игры, здоровье)</li>
 * </ul>
 * 
 * <h3>screen - {@link padej.displayLib.lua.api.ScreenAPI}</h3>
 * <p>Управление экраном:</p>
 * <ul>
 * <li>Получение информации об экране</li>
 * <li>Переключение и закрытие экранов</li>
 * <li>Доступ к виджетам по ID</li>
 * <li>Хранение данных в контексте экрана</li>
 * </ul>
 * 
 * <h3>widget - {@link padej.displayLib.lua.api.WidgetAPI}</h3>
 * <p>Работа с виджетами:</p>
 * <ul>
 * <li>Изменение текста и внешнего вида</li>
 * <li>Управление видимостью и активностью</li>
 * <li>Настройка подсказок</li>
 * <li>Изменение цветов и прозрачности</li>
 * </ul>
 * 
 * <h3>storage - {@link padej.displayLib.lua.api.StorageAPI}</h3>
 * <p>Хранение данных:</p>
 * <ul>
 * <li>Сохранение настроек игрока</li>
 * <li>Хранение состояния между сессиями</li>
 * <li>Работа с ключ-значение парами</li>
 * <li>Поддержка строк, чисел и логических значений</li>
 * </ul>
 * 
 * <h3>timer - {@link padej.displayLib.lua.api.TimerAPI}</h3>
 * <p>Таймеры и отложенные действия:</p>
 * <ul>
 * <li>Выполнение функций через заданное время</li>
 * <li>Повторяющиеся действия</li>
 * <li>Ограниченное количество повторений</li>
 * <li>Отмена таймеров</li>
 * </ul>
 * 
 * <h3>log - {@link padej.displayLib.lua.api.LogAPI}</h3>
 * <p>Логирование:</p>
 * <ul>
 * <li>Информационные сообщения</li>
 * <li>Предупреждения</li>
 * <li>Ошибки</li>
 * <li>Отладочная информация</li>
 * </ul>
 * 
 * <h2>Структура Lua скрипта:</h2>
 * <pre>{@code
 * -- Вызывается при открытии экрана
 * function on_open()
 *     log.info("Экран открыт для " .. player.name())
 *     
 *     -- Инициализация данных
 *     if not storage.has("visit_count") then
 *         storage.set("visit_count", 0)
 *     end
 *     
 *     local visits = storage.get("visit_count")
 *     storage.set("visit_count", visits + 1)
 *     
 *     -- Приветствие
 *     player.message("Добро пожаловать! Визит #" .. (visits + 1))
 * end
 * 
 * -- Обработчик клика по кнопке
 * function onButtonClick()
 *     -- widget доступен автоматически в onClick функциях
 *     widget.text("Нажато!")
 *     widget.bgColor(0, 255, 0)  -- Зеленый фон
 *     
 *     player.sound("ENTITY_EXPERIENCE_ORB_PICKUP")
 *     
 *     -- Автоматически вернуть текст через 2 секунды
 *     timer.after(40, function()
 *         widget.text("Кнопка")
 *         widget.bgColor(50, 50, 50)  -- Серый фон
 *     end)
 * end
 * 
 * -- Вызывается при закрытии экрана
 * function on_close()
 *     log.info("Экран закрыт")
 * end
 * }</pre>
 * 
 * <h2>Контекст выполнения:</h2>
 * <p>Каждый экран имеет изолированный контекст выполнения Lua с доступом
 * только к безопасным функциям. Опасные операции (файловый ввод-вывод,
 * системные вызовы) заблокированы.</p>
 * 
 * @author DisplayLib Team
 * @version 2.0.0
 */
package padej.displayLib.lua.api;